package code.practise.lang.Package;

public class BooleanDemo6 {
	public static void main(String... x){
		boolean b1, b2;
		b1 = true;
		b2 = false;
		String s1, s2;
		s1 = Boolean.toString(b1);
		s2 = Boolean.toString(b2);
		System.out.println("String value of the boolean primitive: "+b1+": is : "+s1);
		System.out.println("String value of the boolean primitive: "+b2+" : is : "+s2);
	}

}
